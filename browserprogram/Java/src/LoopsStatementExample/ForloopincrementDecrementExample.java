package LoopsStatementExample;

public class ForloopincrementDecrementExample {
	public  static void main(String[]args) {
		for(int var=5;var>=2;var--)
		{
			System.out.println(var+"livetech");
		}
		System.out.println("*******");
	for(int a=-3;a>=2;a++)
	{
		System.out.println(a+"jmeter");
	}
	System.out.println("+++++++++");
	
	for(int i=-9;i<=-6;i++)
	{
		System.out.println(i+"load Runner");
	}
	System.out.println("**************");
	
	for(int R=-15;R>=-19;R--)
	{
		System.out.println(R+"QTP");
	}
	System.out.println("########");
	for(int P=10;P>=-10;P=P-3)
	{
		System.out.println(P+"UFT");
	}
	}
	
	
}



