package Decisionmakingloops;

public class NestedifExample2 {
	public static void main(String[]args) {
		for(int i=1;i<=3;i++)
		{
			for(int j=1;j<=2;j++)
			{
				System.out.println(j+"livetech");
			
			}
		}
	System.out.println("@@@@@@@@@");
	for(int a=1;a<=3;a++)
	{
		for(int b=1;b<=a;b++)
		{
			System.out.println(a);
		}
	}
		System.out.println("**********");
	for(int a=1;a<=3;a++)
	{
		for(int b=1;b<=a;b++)
		{
			System.out.println(b);
		}
	}
	System.out.println("#######");
	
	for(int a=1;a<=5;a++)
	{
		for(int b=5;b>=a;b--)
		{
			System.out.print(" *");
		}
	System.out.println();
	}
	}
}



